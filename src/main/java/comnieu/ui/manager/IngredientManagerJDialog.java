    private void loadIngredientToTable() {
        IngredientDAO_1 dao = new IngredientDAO_1();
        List<IngredientImpl_1> list = dao.getAllIngredient();

        DefaultTableModel model = (DefaultTableModel) tbIngredient.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ

        for (IngredientImpl_1 ing : list) {
            model.addRow(new Object[]{
                ing.getId(),         // Mã Nguyên Liệu
                ing.getName(),       // Tên Nguyên Liệu
                ing.getQuantity(),   // Số Lượng
                ing.getUnit(),       // Đơn Vị
                ing.getStatus(),     // Trạng Thái
                ing.getSupplierId(), // Mã NCC
                false                // Checkbox "Chọn"
            });
        }
    }


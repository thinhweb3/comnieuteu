    private void fillToTable() {
        IngredientDAO_1 dao = new IngredientDAO_1();
        List<IngredientImpl_1> list = dao.getAllIngredient();

        DefaultTableModel model = (DefaultTableModel) tblIngredient.getModel();
        model.setRowCount(0);
        for (IngredientImpl_1 ing : list) {
            if ("0".equals(ing.getStatus())) {
                model.addRow(new Object[]{ing.getName(), false});
            }
        }
    }



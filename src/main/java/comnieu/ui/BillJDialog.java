@Override
public void fillBillDetails() {
    String idText = txtId.getText();
    if (idText == null || idText.trim().isEmpty()) {
        details = List.of();
        ((DefaultTableModel) tblBillDetails.getModel()).setRowCount(0);
        return;
    }

    try {
        Long billId = Long.parseLong(idText);
        details = billDetailDao.findByBillId(billId);

        DefaultTableModel model = (DefaultTableModel) tblBillDetails.getModel();
        model.setRowCount(0);
        for (BillDetail d : details) {
            Object[] row = {
                false,
                d.getBillId(),
                d.getDrink().getName(),
                d.getUnitPrice(),
                d.getDiscount(),
                d.getQuantity(),
                d.getTotal()
            };
            model.addRow(row);
        }
    } catch (NumberFormatException ex) {
        XDialog.alert("M√£ phi·∫øu kh√¥ng h·ª£p l·ªá: " + idText);
        ((DefaultTableModel) tblBillDetails.getModel()).setRowCount(0);
    } catch (Exception e) {
        XDialog.alert("L·ªói khi t·∫£i chi ti·∫øt phi·∫øu: " + e.getMessage());
        ((DefaultTableModel) tblBillDetails.getModel()).setRowCount(0);
    }
}


